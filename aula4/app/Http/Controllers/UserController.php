    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = UserMetal::all(); 
        $usersMetal = [];
        
        foreach ($users as $user) {
            $perfil = $user->perfil()->where('id', $user->perfil_id)->first();
            
            $tempUser = $user;
            $tempUser['perfil_name'] = $perfil->name;
            
            $usersMetal[] = $tempUser;
        }
        
        return $usersMetal;
    }

